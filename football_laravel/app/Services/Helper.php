    public static function getFoodyRating($avgRating, $place)
    {
        $rating = [];
        if(isset($place['avg_points']) && count($place['avg_points']) > 0)
        {
            $service = 0;
            foreach($place['avg_points'] as $score)
            {
                $rate = round($score['Point'] / 2, 1);
                switch ($score['Label'])
                {
                    case 'Giá cả':
                        $rating['value'] = $rate;
                        $service += $rate;
                        break;
                    case 'Không gian':
                        $rating['space'] = $rate;
                        $service += $rate;
                        break;
                    case 'Vị trí':
                        $rating['location'] = $rate;
                        $service += $rate;
                        break;
                    default:
                        break;
                }
                $rating['service'] = round($avgRating * 4 - $service, 1);
            }
        }else {
            $rating = null;
        }
        return $rating;
    }

